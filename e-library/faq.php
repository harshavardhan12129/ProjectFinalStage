<h1 id="faq" style="text-align:center;" ><strong>FAQs</strong></h1>



<h1 style="text-align:center;">How can we help?</h1>
<p style="text-align:center;">Not sure where to start your research? Stuck somewhere along the way? Ask a Librarian.
Our librarians provide research tips and strategies to point you in the right direction.</p>

<br>
<h1 style="text-align:center;">What do I do if I get an error message while trying to access an e-library online resource?</h1>
<p style="text-align:center;">It is advisable to try again later, as some databases are licensed for only a certain number of concurrent users. If the error persists please contact library staff.</p>


<br>
<h1 style="text-align:center;">Can I access the e-library resources from home?</h1>
<p style="text-align:center;">Yes, most e-resources are available for access from home or anywhere with an internet connection. Ancestry & Find My Past can only be used at the library branches.</p>
<br>


<br>
<h1 style="text-align:center;">What do the online eLibrary resources contain?, expanded</h1>
<p style="text-align:center;">The online e-resources contain e-books, e-audio books, downloadable music, online magazines, current newspaper articles from the world's leading newspapers; articles from editions of popular magazines; genealogy resources via newspaper databases and the Ancestry database; legal aid assistance; employment assistance and much more. See the e-library for more details.</p>
<br>

<br>
<h1 style="text-align:center;">I am having trouble downloading an e-book/audio book ..., collapsed</h1>
<p style="text-align:center;">Overdrive and Bolinda websites have excellent help screens. For problems with Overdrive, visit this link.  For problems with Bolinda, visit this link then log-in to access the help screen.</p>
<br>

<br>
<h1 style="text-align:center;">Can I download an e-book on a library PC?, expanded</h1>
<p style="text-align:center;">Titles can be borrowed using a library PC but must be downloaded/synced to a device. The Overdrive Download Station located at Mill Park library allows patrons to download directly to that library PC.
There are some titles in the Overdrive collection called Overdrive Read which can be read on a PC (including a library PC). Remember to adhere to PC booking times, if you wish to read a title in the library.</p>
